async function main() {
  const raw = await readFile(STATE_PATH, "utf8");
  const state = JSON.parse(raw);

  const builder = state.builders[0];
  if (!builder) {
    console.error("No builder found in app-state.json");
    process.exit(1);
  }

  // Replace projects in draft
  builder.draft.projects = projects;
  builder.draft.github = {
    profileUrl: "https://github.com/Pcecil21",
    repoUrls: [],
  };
  builder.updatedAt = new Date().toISOString();

  await writeFile(STATE_PATH, JSON.stringify(state, null, 2));

  console.log(`Imported ${projects.length} projects into draft.`);
  console.log("Refresh the Studio page in your browser to see them.");
  console.log("Then click 'Republish' to make them public.");
}

